package org.pabba.happycube;

public class Cube_Corner {
    private char x;
    private char y;
    private char z;
    public Cube_Corner(char x,char y,char z)
    {
        this.x=x;
        this.y=y;
        this.z=z;
    }
    public char getX(){return  x;}
    public char getY(){return y;}
    public char getZ(){return z;}
}
